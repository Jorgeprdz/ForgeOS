import * as XLSX from 'xlsx';

export async function parsearExcel({

    file

}) {

    /*
    |--------------------------------------------------------------------------
    | Leer archivo
    |--------------------------------------------------------------------------
    */

    const buffer =
        await file.arrayBuffer();

    /*
    |--------------------------------------------------------------------------
    | Workbook
    |--------------------------------------------------------------------------
    */

    const workbook =

        XLSX.read(

            buffer,

            {
                type: 'array'
            }
        );

    /*
    |--------------------------------------------------------------------------
    | Primera hoja
    |--------------------------------------------------------------------------
    */

    const sheetName =

        workbook.SheetNames[0];

    const worksheet =

        workbook.Sheets[
            sheetName
        ];

    /*
    |--------------------------------------------------------------------------
    | Convertir JSON
    |--------------------------------------------------------------------------
    */

    const rows =

        XLSX.utils.sheet_to_json(
            worksheet,
            {
                defval: ''
            }
        );

    return {

        sheetName,

        totalRows:
            rows.length,

        rows
    };
}