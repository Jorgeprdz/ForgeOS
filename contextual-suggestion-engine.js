/*
|--------------------------------------------------------------------------
| MODULE: contextual-suggestion-engine.js
|--------------------------------------------------------------------------
|
| Contextual operational suggestions.
|
|--------------------------------------------------------------------------
*/

export function generarSugerenciasContextuales({

    context = {}

}) {

    const suggestions = [];

    /*
    |--------------------------------------------------------------------------
    | Renewal
    |--------------------------------------------------------------------------
    */

    if (

        context.renewalClose
    ) {

        suggestions.push(

            '/renovaciones'
        );
    }

    /*
    |--------------------------------------------------------------------------
    | Risk
    |--------------------------------------------------------------------------
    */

    if (

        context.riskLevel
        === 'HIGH'
    ) {

        suggestions.push(

            '/riesgo'
        );
    }

    return suggestions;
}