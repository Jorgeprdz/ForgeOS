/*
|--------------------------------------------------------------------------
| MODULE: action-resolver-engine.js
|--------------------------------------------------------------------------
|
| Operational action resolver.
|
|--------------------------------------------------------------------------
*/

export function resolverAccion({

    input = ''

}) {

    const normalized =
        input.toLowerCase();

    /*
    |--------------------------------------------------------------------------
    | WhatsApp
    |--------------------------------------------------------------------------
    */

    if (

        normalized.includes(
            'whatsapp'
        )
    ) {

        return 'WHATSAPP';
    }

    /*
    |--------------------------------------------------------------------------
    | Call
    |--------------------------------------------------------------------------
    */

    if (

        normalized.includes(
            'llamar'
        )
    ) {

        return 'CALL';
    }

    /*
    |--------------------------------------------------------------------------
    | Renewal
    |--------------------------------------------------------------------------
    */

    if (

        normalized.includes(
            'renovación'
        )
    ) {

        return 'RENEWALS';
    }

    return null;
}