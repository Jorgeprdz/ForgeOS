export function calcularPerformance({

    puntos = 0,

    cierres = 0,

    llamadas = 0

}) {

    let score = 0;

    /*
    |--------------------------------------------------------------------------
    | Puntos
    |--------------------------------------------------------------------------
    */

    score += puntos * 2;

    /*
    |--------------------------------------------------------------------------
    | Cierres
    |--------------------------------------------------------------------------
    */

    score += cierres * 15;

    /*
    |--------------------------------------------------------------------------
    | Llamadas
    |--------------------------------------------------------------------------
    */

    score += llamadas;

    return {

        score,

        nivel:
            obtenerNivel(score)
    };
}

function obtenerNivel(

    score

) {

    if (score >= 200) {

        return 'elite';
    }

    if (score >= 120) {

        return 'alto';
    }

    if (score >= 60) {

        return 'medio';
    }

    return 'bajo';
}