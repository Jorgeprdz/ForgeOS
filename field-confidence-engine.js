/*
|--------------------------------------------------------------------------
| MODULE: field-confidence-engine.js
|--------------------------------------------------------------------------
|
| Confidence scoring OCR/AI.
|
|--------------------------------------------------------------------------
*/

export function calcularConfidence({

    value = '',

    field = ''

}) {

    /*
    |--------------------------------------------------------------------------
    | Sin valor
    |--------------------------------------------------------------------------
    */

    if (!value) {

        return 0;
    }

    /*
    |--------------------------------------------------------------------------
    | Reglas básicas
    |--------------------------------------------------------------------------
    */

    switch (field) {

        case 'cliente':

            return value.length >= 5
                ? 95
                : 60;

        case 'prima':

            return value.includes('$')
                ? 98
                : 70;

        case 'numeroPoliza':

            return value.length >= 6
                ? 92
                : 50;

        default:

            return 75;
    }
}