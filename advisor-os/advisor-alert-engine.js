export function generarAlertaAsesor({

    renewalClose = false,

    overdueTasks = 0

}) {

    const alerts = [];

    /*
    |--------------------------------------------------------------------------
    | Renewal
    |--------------------------------------------------------------------------
    */

    if (renewalClose) {

        alerts.push({

            type:
                'RENEWAL',

            message:
                'Tienes renovaciones próximas'
        });
    }

    /*
    |--------------------------------------------------------------------------
    | Tasks
    |--------------------------------------------------------------------------
    */

    if (overdueTasks > 0) {

        alerts.push({

            type:
                'TASKS',

            message:
                'Tienes tareas vencidas'
        });
    }

    return alerts;
}