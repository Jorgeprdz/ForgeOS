/*
|--------------------------------------------------------------------------
| MODULE: phone-call-engine.js
|--------------------------------------------------------------------------
|
| Sistema rápido de llamadas.
|
|--------------------------------------------------------------------------
*/

export function llamarLead({

    phone = '',

    countryCode = '52'

}) {

    /*
    |--------------------------------------------------------------------------
    | Limpiar número
    |--------------------------------------------------------------------------
    */

    let cleanPhone =

        phone.replace(
            /\D/g,
            ''
        );

    /*
    |--------------------------------------------------------------------------
    | Prefijo MX
    |--------------------------------------------------------------------------
    */

    if (
        !cleanPhone.startsWith(
            countryCode
        )
    ) {

        cleanPhone =
            countryCode
            + cleanPhone;
    }

    /*
    |--------------------------------------------------------------------------
    | Ejecutar llamada
    |--------------------------------------------------------------------------
    */

    window.location.href =
`
tel:${cleanPhone}
    `.trim();
}