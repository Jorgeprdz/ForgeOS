/*
|--------------------------------------------------------------------------
| MODULE: lead-temperature-engine.js
|--------------------------------------------------------------------------
|
| Sistema de temperatura comercial.
|
|--------------------------------------------------------------------------
*/

export function calcularTemperaturaLead({

    ultimaRespuestaDias = 0,

    citas = 0,

    interes = 'medio'

}) {

    let score = 0;

    /*
    |--------------------------------------------------------------------------
    | Actividad reciente
    |--------------------------------------------------------------------------
    */

    if (ultimaRespuestaDias <= 1) {

        score += 40;

    } else if (
        ultimaRespuestaDias <= 3
    ) {

        score += 25;

    } else {

        score += 5;
    }

    /*
    |--------------------------------------------------------------------------
    | Interacción comercial
    |--------------------------------------------------------------------------
    */

    score += citas * 10;

    /*
    |--------------------------------------------------------------------------
    | Interés
    |--------------------------------------------------------------------------
    */

    switch (interes) {

        case 'alto':
            score += 40;
            break;

        case 'medio':
            score += 20;
            break;

        case 'bajo':
            score += 5;
            break;
    }

    /*
    |--------------------------------------------------------------------------
    | Resultado
    |--------------------------------------------------------------------------
    */

    if (score >= 80) {

        return 'hot';
    }

    if (score >= 50) {

        return 'warm';
    }

    return 'cold';
}