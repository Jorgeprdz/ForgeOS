export class EventBus {

    constructor() {

        this.listeners = {};
    }

    /*
    |--------------------------------------------------------------------------
    | Subscribe
    |--------------------------------------------------------------------------
    */

    on(event, callback) {

        if (!this.listeners[event]) {

            this.listeners[event] = [];
        }

        this.listeners[event].push(
            callback
        );
    }

    /*
    |--------------------------------------------------------------------------
    | Emit
    |--------------------------------------------------------------------------
    */

    emit(event, payload) {

        if (!this.listeners[event]) {

            return;
        }

        for (

            const callback
            of this.listeners[event]
        ) {

            callback(payload);
        }
    }
}