import {

    detectarCampos

} from './smart-field-detection-engine.js';

export function generarMappingMasivo({

    rows = []

}) {

    /*
    |--------------------------------------------------------------------------
    | Sin rows
    |--------------------------------------------------------------------------
    */

    if (!rows.length) {

        return {};
    }

    /*
    |--------------------------------------------------------------------------
    | Obtener headers
    |--------------------------------------------------------------------------
    */

    const headers =
        Object.keys(rows[0]);

    /*
    |--------------------------------------------------------------------------
    | Smart detection
    |--------------------------------------------------------------------------
    */

    return detectarCampos(
        headers
    );
}