export function resolverCanal({

    priority = 'NORMAL'

}) {

    /*
    |--------------------------------------------------------------------------
    | Critical
    |--------------------------------------------------------------------------
    */

    if (priority === 'CRITICAL') {

        return [

            'PUSH',

            'IN_APP'
        ];
    }

    /*
    |--------------------------------------------------------------------------
    | Default
    |--------------------------------------------------------------------------
    */

    return [

        'IN_APP'
    ];
}